DROP TABLE IF EXISTS REQUESTS CASCADE;
DROP TABLE IF EXISTS BOOKINGS CASCADE;
DROP TABLE IF EXISTS COMMENTS CASCADE;
DROP TABLE IF EXISTS ITEMS CASCADE;
DROP TABLE IF EXISTS USERS CASCADE;


CREATE TABLE IF NOT EXISTS users
(
    id    INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name  VARCHAR(16)        NOT NULL,
    email VARCHAR(32) UNIQUE NOT NULL
);


CREATE TABLE IF NOT EXISTS requests
(
    id           INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    description  VARCHAR(64)                                 NOT NULL,
    created      TIMESTAMP WITHOUT TIME ZONE                 NOT NULL DEFAULT CURRENT_TIMESTAMP,
    requestor_id INT REFERENCES users (id) ON DELETE CASCADE NOT NULL
);


CREATE TABLE IF NOT EXISTS items
(
    id           INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         VARCHAR(32)                                 NOT NULL,
    description  VARCHAR(64)                                 NOT NULL,
    is_available BOOLEAN                                     NOT NULL,
    owner_id     INT REFERENCES users (id) ON DELETE CASCADE NOT NULL,
    request_id   INT REFERENCES requests (id) ON DELETE CASCADE,
    last_booking TIMESTAMP WITHOUT TIME ZONE                 NOT NULL DEFAULT CURRENT_TIMESTAMP,
    next_booking TIMESTAMP WITHOUT TIME ZONE                 NOT NULL DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE IF NOT EXISTS bookings
(
    id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_date TIMESTAMP WITHOUT TIME ZONE,
    end_date   TIMESTAMP WITHOUT TIME ZONE,
    item_id    INT REFERENCES items (id) ON DELETE CASCADE,
    booker_id  INT REFERENCES users (id) ON DELETE CASCADE,
    status     VARCHAR(16),

);


CREATE TABLE IF NOT EXISTS comments
(
    id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text      VARCHAR(2048) NOT NULL,
    item_id   INT REFERENCES items (id) ON DELETE CASCADE,
    author_id INT REFERENCES users (id) ON DELETE CASCADE,
    created   TIMESTAMP
);
